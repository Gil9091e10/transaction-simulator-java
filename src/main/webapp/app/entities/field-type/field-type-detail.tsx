import React, { useEffect } from 'react';
import { Link, useParams } from 'react-router-dom';
import { Button, Col, Row } from 'reactstrap';
import { Translate } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { useAppDispatch, useAppSelector } from 'app/config/store';

import { getEntity } from './field-type.reducer';

export const FieldTypeDetail = () => {
  const dispatch = useAppDispatch();

  const { id } = useParams<'id'>();

  useEffect(() => {
    dispatch(getEntity(id));
  }, []);

  const fieldTypeEntity = useAppSelector(state => state.fieldType.entity);
  return (
    <Row>
      <Col md="8">
        <h2 data-cy="fieldTypeDetailsHeading">
          <Translate contentKey="simulatorApp.fieldType.detail.title">FieldType</Translate>
        </h2>
        <dl className="jh-entity-details">
          <dt>
            <span id="id">
              <Translate contentKey="global.field.id">ID</Translate>
            </span>
          </dt>
          <dd>{fieldTypeEntity.id}</dd>
          <dt>
            <span id="name">
              <Translate contentKey="simulatorApp.fieldType.name">Name</Translate>
            </span>
          </dt>
          <dd>{fieldTypeEntity.name}</dd>
        </dl>
        <Button tag={Link} to="/field-type" replace color="info" data-cy="entityDetailsBackButton">
          <FontAwesomeIcon icon="arrow-left" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.back">Back</Translate>
          </span>
        </Button>
        &nbsp;
        <Button tag={Link} to={`/field-type/${fieldTypeEntity.id}/edit`} replace color="primary">
          <FontAwesomeIcon icon="pencil-alt" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.edit">Edit</Translate>
          </span>
        </Button>
      </Col>
    </Row>
  );
};

export default FieldTypeDetail;
